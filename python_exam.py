# 3. Циклы while и for
# Чем отличается while от for?
# В каких случаях лучше использовать while?
# Цикл While работает до тех пор пока значение истинно 
# Цикл For работает в определенном диапазоне
# Цикл While лучше использовать в проекте, где нужно зациклить какую-то форму, например, меню.

