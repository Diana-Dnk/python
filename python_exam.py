# 4. Функция range()
# Что делает range(n)?
# Какие существуют варианты range(start, stop, step)?
# range(n) используется для циклов и пробегает по заданному диапазону, от 0 до n не включительно. 
# range(start, stop, step)означает откуда начинается цикл, где заканчивается и какой шаг у цикла. 
# Например: 
# for i in range(1,5,2):
#     print(i)
