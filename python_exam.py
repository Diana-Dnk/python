# 10. Инкапсуляция и наследование
# Что такое инкапсуляция и наследование?
# Приведи короткий пример: кто от кого наследуется.
# Инкапсуляция нужна для защиты данных 
# . – публичный. Любой может изменить 
# _-протектед. Можно изменить, но не стоит этого делать
# __- приватный. Никто не может изменить 
# Наследование используется для наследование класса родителя, например при создании героев в игре. Но при этом после наследования класса родителя можно прописать логику только этого персонажа. 
