# Теория
# 1. Условия if / elif / else
# Чем отличается if, elif, else?
# В каком случае используется elif?
# if / elif / else используются для того, чтобы задавать условия. 
# Условий может быть неограниченное количество. if - если, самое первое условие, через которое проходит код.  
# elif - иначе если, дополнительное условие в случае если if не сработал, else – иначе, заключительное условие, в случае если не сработал if  и elif. 
# Elif используется для дополнительных вариантов условий. 
# ________________________________________
# 2. Логические операторы
# В чём разница между and, or, not?
# Приведи пример условия с использованием and.
# And –  оператор “и” связывает два условия, для получения ответа оба условия должны сработать
# Or – оператор «или», также связывает два условия, однако для получения должно сработать хотя бы одно из условий. 
# not  - оператор «не», предеает отрицательное условие
# пример: if age>=18 and has_ticket=="да":
# 		print("Проход разрешен")

